<button>¿Qué es Listado Oficial ó Ingreso a la Docencia y cuando debo inscribirme?</button>

<p>El Listado Oficial es una lista ordenada por puntaje de mayor a menor dividida en ramas y cargos que incluye todos los docentes en condiciones de titularizar un cargo en el sistema. Es decir los inscriptos poseen Título habilitante. La inscripción del Listado Oficial es en Abril/Mayo de un año para ser usado efectivamente el año siguiente.</p>
<button>¿Qué son los listados "in fine" y cuando debo inscribirme?</button>

<p>Hasta ahora siempre hablamos de Listados donde uno se inscribe este año para ser usado efectivamente el año siguiente, pero que pasa si un aspirante se quiere inscribir este año para trabajar este mismo año, este es el caso del listado "in fine". El docente se inscribe y es valorado en el corto plazo para poder participar de actos públicos. Se debe inscribir en la Secretaría de Inspección correspondiente, las fechas y horarios dependen de ese organismo.</p>
<button>¿Es lo mismo un 108A ó B que un 108A ó B in fine?</button>
<p>Definitivamente NO, en el 108A ó 108B el aspirante se anota este año para participar de actos públicos recién el año que viene mientras que el "in fine" esperará un corto plazo de valoración y puede participar de Actos Públicos el mismo año que se inscribe.</p>
<button>¿Qué otros listados existen?</button>
<p>Existen otros Listados como Emergencia (se abre cuando no es posible cubrir con lo que hay) Listado de Superior (se usa para los Institutos superiores) Listado de Superior Artítica (también se usa para Institutos superiores)</p>
<button>¿Con qué listado se puede Titularizar?</button>
<p>Sólo es posible Titularizar con el Listado Oficial, los otros listados son puramente para provisionalidades y suplencias.</p>



